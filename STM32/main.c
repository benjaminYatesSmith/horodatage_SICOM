static void GPS_ReadOneFrame(void)
{
    char buffer[100];
    HAL_StatusTypeDef status = HAL_UART_Receive(&huart4, (uint8_t*)buffer, 80, 2000);

    if(status == HAL_OK)
    {
    	//afficher_minutes(53);
        buffer[79] = '\0';  // S'assurer que la chaîne est terminée
        char* ptr = strstr(buffer, "$");
        if(ptr != NULL)
        {
            char heure_str[7] = {0};
            strncpy(heure_str, ptr + 7, 6);  // Extraction de "hhmmss"
            heure_str[6] = '\0'; // Terminaison de la chaîne

            uint8_t hh = (heure_str[0] - '0') * 10 + (heure_str[1] - '0');
            uint8_t mm = (heure_str[2] - '0') * 10 + (heure_str[3] - '0');
            uint8_t ss = (heure_str[4] - '0') * 10 + (heure_str[5] - '0');

            // Stocker les valeurs extraites dans vos variables globales
            g_heures   = hh;
            g_minutes  = mm;
            g_secondes = ss;
        }
    }
    else
    {
        // Gestion d'erreur ou retenter la lecture
    }
}



